package ru.practicum.shareit.booking.model;

/**
 * Состояния бронирования для фильтрации запросов.
 * Используется для получения списка бронирований в определённом статусе или временном периоде.
 */
public enum BookingState {
    /**
     * Все бронирования (без фильтрации).
     */
    ALL,

    /**
     * Текущие бронирования (начались, но ещё не завершились).
     */
    CURRENT,

    /**
     * Будущие бронирования (ещё не начались).
     */
    FUTURE,

    /**
     * Завершённые бронирования (уже закончились).
     */
    PAST,

    /**
     * Бронирования, ожидающие подтверждения (статус WAITING).
     */
    WAITING,

    /**
     * Отклонённые бронирования (статус REJECTED).
     */
    REJECTED
}